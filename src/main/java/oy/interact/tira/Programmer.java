package oy.interact.tira;

public class Programmer {

	private String name;
	private String wuTangName;

	public Programmer(String name, String wuTangName) {
		this.name = name;
		this.wuTangName = wuTangName;
	}

	public String getName() {
		return name;
	}

	public String getWuTangName() {
		return wuTangName;
	}

}
